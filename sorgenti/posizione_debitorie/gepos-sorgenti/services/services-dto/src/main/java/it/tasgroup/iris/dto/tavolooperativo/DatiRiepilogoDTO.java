package it.tasgroup.iris.dto.tavolooperativo;

public class DatiRiepilogoDTO {
	
	private String sil;
	private String soggetto;
	private String statoAPE;
	private String statoAP;
	private String statoIP;
	private String messaggioAPE;
	private String messaggioAP;
	private String messaggioIP;
	
	
	
	public String getSil() {
		return sil;
	}
	public void setSil(String sil) {
		this.sil = sil;
	}
	public String getSoggetto() {
		return soggetto;
	}
	public void setSoggetto(String soggetto) {
		this.soggetto = soggetto;
	}
	public String getStatoAPE() {
		return statoAPE;
	}
	public void setStatoAPE(String statoAPE) {
		this.statoAPE = statoAPE;
	}
	public String getStatoAP() {
		return statoAP;
	}
	public void setStatoAP(String statoAP) {
		this.statoAP = statoAP;
	}
	public String getStatoIP() {
		return statoIP;
	}
	public void setStatoIP(String statoIP) {
		this.statoIP = statoIP;
	}
	public String getMessaggioAPE() {
		return messaggioAPE;
	}
	public void setMessaggioAPE(String messaggioAPE) {
		this.messaggioAPE = messaggioAPE;
	}
	public String getMessaggioAP() {
		return messaggioAP;
	}
	public void setMessaggioAP(String messaggioAP) {
		this.messaggioAP = messaggioAP;
	}
	public String getMessaggioIP() {
		return messaggioIP;
	}
	public void setMessaggioIP(String messaggioIP) {
		this.messaggioIP = messaggioIP;
	}

}
