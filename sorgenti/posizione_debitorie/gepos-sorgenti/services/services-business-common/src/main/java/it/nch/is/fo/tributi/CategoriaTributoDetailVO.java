package it.nch.is.fo.tributi;

public class CategoriaTributoDetailVO {
	
	private String idTributo;
	private String denominazione;
	private String tipoEntrata;
	private String predeterminabile;
	private String iniziativaDebitore;

	public String getIdTributo() {
		return idTributo;
	}
	public void setIdTributo(String idTributo) {
		this.idTributo = idTributo;
	}
	public String getDenominazione() {
		return denominazione;
	}
	public void setDenominazione(String denominazione) {
		this.denominazione = denominazione;
	}
	public String getTipoEntrata() {
		return tipoEntrata;
	}
	public void setTipoEntrata(String tipoEntrata) {
		this.tipoEntrata = tipoEntrata;
	}
	public String getPredeterminabile() {
		return predeterminabile;
	}
	public void setPredeterminabile(String predeterminabile) {
		this.predeterminabile = predeterminabile;
	}
	public String getIniziativaDebitore() {
		return iniziativaDebitore;
	}
	public void setIniziativaDebitore(String iniziativaDebitore) {
		this.iniziativaDebitore = iniziativaDebitore;
	}
	
	
}
